package _16__Arrays;

public class ArrayCreation {
    public static void main(String[] args) {
        int[] marks = new int[5];
        marks[0] = 90;
        marks[1] = 56;
        marks[2] = 34;
        marks[3] = 50;
        marks[4] = 79;
        System.out.println(marks[0]);
        System.out.println(marks[1]);
        System.out.println(marks[2]);
        System.out.println(marks[3]);
        System.out.println(marks[4]);
    }
}
